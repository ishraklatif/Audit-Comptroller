                  <p>Performance Audit has been  incorporated in the audit portfolio of the Office of the Comptroller &amp;  Auditor General of Bangladesh  [OCAG] since 1999. The OCAG realizes that in Bangladesh, where resource  constraint is a major issue and a significant amount of public expenditure is  financed by public debt, an independent evaluation of the economy, efficiency  and effectiveness with which public resources have been used is essential.  Therefore, Performance Audit was introduced by OCAG in addition to Financial  and Compliance Audits for ensuring accountability of the executive to the  Parliament and ultimately to the taxpayers for optimal utilization of public  resources. </p>

                  <p>The Constitution of the  Peoples’ Republic   of Bangladesh provides  OCAG with the mandate to perform Performance Audits. The mandate of the CAG, to  carry out all audits, is derived from Article 128 of the Constitution which provides  the CAG with discretionary power in deciding how, what and when to audit. <br>

                    Moreover, the Public  Accounts Committee [PAC] of the 5th Parliament expressed its concern  in 1998 for greater accountability and requested the Auditor General to conduct  Performance Audits alongside annual financial and compliance audits.</p>

                  <p>&nbsp;</p>

                  <p><strong>Performance Audit Activities  in OCAG</strong></p>

                  <p>Performance Audit was first  piloted by OCAG in 1999 in response to the demand of the PAC.  A Performance Audit Cell was created and four Pilot Audits were carried out by  the Cell with officials who had under gone extensive trainings in the relevant  fields. The reports were finalized in January 2002 and were submitted to  Parliament. The reports are:</p>

                  <ul>

                    <li>Performance Audit on Printing, Publication and  Distribution of Text Books</li>

                    <li>Performance Audit on the Power Distribution System  of Dhaka Electricity Supply Authority [DESA]</li>

                    <li>Performance Audit of the Industrial Parks of  Bangladesh Small and Cottage Industries Corporation [BSCIC]</li>

                    <li>Performance Audit on the Health Care Services of  the Chittagong Medical College   Hospital</li>

                  </ul>

                  <p>After the successful  piloting of Performance Audit in Bangladesh, OCAG conducted performance  audits regularly through the nine Audit Directorates. </p>

                  <p>Then in 2005 a Performance  Audit Directorate was established to coordinate performance audit activities of  OCAG. The Organogram of the Performance Audit Directorate is given below:</p>

                  <p align="center"><img src="images/perfoaudchart.jpg" width="500" height="304" alt="Organogram of the Performance Audit Directorate"></p>

                  <p>The main responsibility  assigned to the Performance Audit Directorate is the enhancement of performance  audit capability of OCAG by:</p>

                  <ul>

                    <li>Providing advice to other Audit Directorates </li>

                    <li>Supervising performance audit works of other Audit  Directorate</li>

                    <li>Conducting performance audits</li>

                    <li>Working as research wing of OCAG for improving  audit methodology</li>

                    <li>Developing training modules and deliver trainings</li>

                    <li>Developing Quality Checklists</li>

                    <li>Setting up a Resource Centre </li>

                  </ul>

