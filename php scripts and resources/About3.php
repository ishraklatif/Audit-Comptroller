                  <p><strong>Capacity</strong><strong> Building</strong><strong> Measures:</strong><br>

                    Several  measures have been taken by the OCAG to enhance its capability for conducting Performance  Audits. These are:</p>

                  <ul type="disc">

                    <li>Formation of the Performance Audit Advisory       Committee</li>

                    <li>A Performance Audit Core Group has been formed       comprising of 28 officers of the department for carrying out performance       audit in a professional manner with the help of SCOPE Project. Pilot       performance audit will be carried out in guidance of International experts       in this sector.</li>

                    <li>Performance Audit Training provided by the       Performance Audit Directorate in collaboration with in FIMA to 90 selected       officials during 2008-2009</li>

                    <li>Training on Field Execution &amp; Reporting       will be carried out by Performance Audit Directorate for members of       Performance Audit Teams who will be conducting Performance Audit in 2010. </li>

                  </ul>

                  <p>The  OCAG is continuously trying to increase the effectiveness of its audit function  and the on going reforms the OCAG has undertaken will gradually improve the performance  audit activities in line with the international best practices.</p>

