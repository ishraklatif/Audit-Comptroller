<p>Mohammad Muslim Chowdhury, 12th Comptroller and Auditor General of Bangladesh is a highly accomplished person with an illustrious career in the civil service spanning across 33 years. Having an in-depth and broad-based knowledge and experience on matters of Public Financial Management, Administration and Governance, he was a member of Bangladesh Civil Service (Audit and Accounts Cadre) 1984 batch. Prior to assuming the office of Comptroller and Auditor General of Bangladesh on 17th July, 2018, Mr. Chowdhury was Finance Secretary. Before his elevation to the position of Secretary to the Government of Bangladesh, he had served as Deputy Secretary, Joint Secretary and Additional Secretary in the Finance Division, Ministry of Finance. He had also held various positions under the Office of the Comptroller and Auditor General, Controller General of Accounts and Controller General Defence Finance.</p>
<p><br>
Academically, he holds a Master of Science (MSc) in Finance and Accounting with distinction from the University of Birmingham, UK. He graduated with a Bachelor degree in Commerce with Honours and Master of Commerce in Accounting from the University of Chittagong, Bangladesh.</p>
<p><br>
During his long and illustrious career in the civil service, Mr. Chowdhury had pioneered the Public Sector Financial Management Reform and was awarded the “Jana Prashashan (Public Administration) Award, 2017” for his contribution to the improvement of Public Service delivery system. He was passionate about introducing e-Governance in Finance and engaged in a supervisory role at the IFMIS (iBAS++) development and implementation process. He led from the front in the development of "Government Pensioners Database" and "Government Employees Database" which brought discipline in the Government Pension and Salary management. He was actively involved in the initial formulation and implementation of PPP Framework in Bangladesh and drafted the PPP Strategy and Policy issued by the Government of Bangladesh. He had steered the formulation of New Budget and Accounting Classification System (BACS) in the Government planning, budgeting and accounting from Fiscal Year 2018-19.</p>
